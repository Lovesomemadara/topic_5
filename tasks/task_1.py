numbers: list = [17, 6.06534, 91, 52, 87, 340, 56]

# Сумма всех элементов списка
summ: int = sum(numbers)

# Нахождение максимума в списке
maxim: int = max(numbers)

# Нахождение минимума в списке
minim: int = min(numbers)

print(
    "Сумма элементов списка:", summ,
    "\nМаксимальный элемент списка:", maxim,
    "\nМинимальный элемент списка:", minim
)
